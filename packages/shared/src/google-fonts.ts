/** Font families grouped by category. */
export const GOOGLE_FONTS_BY_CATEGORY: Record<string, string[]> = {};
for (const f of GOOGLE_FONTS) {
  const list = GOOGLE_FONTS_BY_CATEGORY[f.category];
  if (list) {
    list.push(f.family);
  } else {
    GOOGLE_FONTS_BY_CATEGORY[f.category] = [f.family];
  }
}

/**
 * Build a Google Fonts CSS2 URL to load one or more font families.
 * Uses the variable-axis API for optimal file sizes.
 */
export function buildGoogleFontsUrl(families: string[]): string {
  const unique = [...new Set(families.filter(Boolean))];
  if (unique.length === 0) return '';
  const params = unique
    .map((f) => `family=${encodeURIComponent(f)}:wght@300;400;500;600;700`)
    .join('&');
  return `https://fonts.googleapis.com/css2?${params}&display=swap`;
}
